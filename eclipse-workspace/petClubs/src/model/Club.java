package model;

import java.util.ArrayList;
import java.util.GregorianCalendar;

public class Club {
	
	private String id;
	private String name;
	private ArrayList<Person> clients;
	private GregorianCalendar issueDate;
	
	
	public Club(String id, String name, ArrayList<Person> clients, GregorianCalendar issueDate) {
		super();
		this.id = id;
		this.name = name;
		this.clients = clients;
		this.issueDate = issueDate;
	}
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public ArrayList<Person> getClients() {
		return clients;
	}
	public void setClients(ArrayList<Person> clients) {
		this.clients = clients;
	}


	public GregorianCalendar getIssueDate() {
		return issueDate;
	}


	public void setIssueDate(GregorianCalendar issueDate) {
		this.issueDate = issueDate;
	}
	
	
	
	


} //end of class
